/**
 * AGL glyph name → Unicode string, handling period suffixes and underscore ligatures.
 * @param {string} glyphName
 */
export function aglLookup(glyphName) {
  // Direct lookup — value is a number (single codepoint) or string (multi-codepoint)
  const direct = aglMap[glyphName];
  if (direct !== undefined) return typeof direct === 'string' ? direct : String.fromCodePoint(direct);

  // Strip period suffix (e.g. "one.oldstyle" → "one")
  const dotIdx = glyphName.indexOf('.');
  if (dotIdx > 0) {
    const baseName = glyphName.substring(0, dotIdx);
    const base = aglMap[baseName];
    if (base !== undefined) return typeof base === 'string' ? base : String.fromCodePoint(base);
  }

  // "uni" prefix: uniXXXX or uniXXXXYYYY... (groups of 4 hex digits)
  const uniMatch = /^uni([0-9A-Fa-f]{4,})$/.exec(glyphName);
  if (uniMatch) {
    const hex = uniMatch[1];
    if (hex.length % 4 === 0) {
      let result = '';
      for (let i = 0; i < hex.length; i += 4) {
        result += String.fromCodePoint(parseInt(hex.substring(i, i + 4), 16));
      }
      return result;
    }
  }

  // PostScript "C" prefix: C0065 → charCode 65 → "A". Some PDF producers (e.g., Bosch
  // manuals) use /Differences with glyph names like C0097, C0065, C0034 where the number
  // is the standard character code. Map printable ASCII range (32-255) to their character.
  const cMatch = /^C(\d{1,5})$/.exec(glyphName);
  if (cMatch) {
    const code = Number(cMatch[1]);
    if (code >= 32 && code <= 255) return String.fromCharCode(code);
  }

  // Underscore ligatures (e.g. "f_f_i" → "ffi")
  if (glyphName.includes('_')) {
    const parts = glyphName.split('_');
    let result = '';
    for (const part of parts) {
      const val = aglMap[part];
      if (val === undefined) return null;
      result += typeof val === 'string' ? val : String.fromCodePoint(val);
    }
    return result;
  }

  return null;
}

